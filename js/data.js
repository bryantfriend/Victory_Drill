// Transliteration helper
const transliterate = (text) => {
    return text.toLowerCase()
        .replace(/ё/g, 'yo')
        .replace(/ж/g, 'zh')
        .replace(/ч/g, 'ch')
        .replace(/ш/g, 'sh')
        .replace(/щ/g, 'shch')
        .replace(/ю/g, 'yu')
        .replace(/я/g, 'ya')
        .replace(/[ьъ]/g, '')
        .replace(/а/g, 'a')
        .replace(/б/g, 'b')
        .replace(/в/g, 'v')
        .replace(/г/g, 'g')
        .replace(/д/g, 'd')
        .replace(/е/g, 'e')
        .replace(/з/g, 'z')
        .replace(/и/g, 'i')
        .replace(/й/g, 'y')
        .replace(/к/g, 'k')
        .replace(/л/g, 'l')
        .replace(/м/g, 'm')
        .replace(/н/g, 'n')
        .replace(/о/g, 'o')
        .replace(/п/g, 'p')
        .replace(/р/g, 'r')
        .replace(/с/g, 's')
        .replace(/т/g, 't')
        .replace(/у/g, 'u')
        .replace(/ф/g, 'f')
        .replace(/х/g, 'h')
        .replace(/ц/g, 'ts')
        .replace(/ы/g, 'y')
        .replace(/э/g, 'e');
};

// Enhanced word helper
const word = (t, folder, stress, syllables, sound, pos = 'noun') => {
    const file = transliterate(t);
    return {
        t,
        file,
        i: `assets/${folder}/${file}.jpg`,
        stress,
        syllables,
        sound,
        pos
    };
};

